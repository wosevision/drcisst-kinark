						<h4>“Compassion Fatigue Glossary”</h4>
						<p><b>Stress:</b> The nonspecific response of the human organism to any demand placed upon it.</p>
						<p><b>Suffering:</b> A state of severe distress* associated with events that threaten the intactness of the person.</p>
						<p><b>Compassion:</b> Deep awareness of the suffering of another coupled with the wish to relieve it.</p>
						<p><b>Compassionate Mission:</b> Any endeavor by individuals, groups of individuals or organizations of alleviate the suffering of others.</p>
						<p><b>Compassion Fatigue:</b> 1) A state of tension and preoccupation with the individual or cumulative trauma of clients as manifested in one or more ways including re-experiencing the traumatic event, avoidance/numbing of reminders of the event, and persistent arousal. 2) The natural consequence of stress resulting from caring for and helping traumatized or suffering people or animals.</p>
						<p><b>Burnout:</b> a state associated with stress and hassles involved in your vocation.</p>

