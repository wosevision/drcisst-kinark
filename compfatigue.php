						<h4>Symptoms of Compassion Fatigue (intrusive)</h4>
						<ul><li>Thoughts and images associated with the client’s problems and pain.</li>
						<li> Obsessive or compulsive desire to help certain clients</li>
						<li>Client/work issues encroaching on personal time</li>
						<li>Inability to “let go” of mission related matters</li>
						<li>Perception of clients as fragile and needing your assistance</li>
						<li>Sense of inadequacy • Sense of entitlement</li>
						<li>Perception of the world in terms of victims and perpetrators</li>
						<li>Personal activities interrupted by the mission</li></ul>

						<h4>Symptoms of Compassion Fatigue (avoidance)</h4>
						<ul><li>Silencing Response (avoiding client’s stuff)</li>
						<li>Loss of enjoyment/cessation of self-care activities</li>
						<li>Loss of energy</li>
						<li>Loss of hope/sense of dread working with certain clients</li>
						<li>Loss of sense of competence/potency</li>
						<li>Isolation</li>
						<li>Secretive self-medication/addition (alcohol, drugs, work, sex, food, spending, etc.)</li>
						<li>Relationaldysfunction</li></ul>

						<h4>Symptoms of Compassion Fatigue (arousal)</h4>
						<ul><li>Increased anxiety</li>
						<li>Impulsivity/reactivity</li>
						<li>Increased perception of demand/threat (in both job and environment) </li>
						<li>Increased frustration/anger</li>
						<li>Sleep disturbance</li>
						<li>Difficulty concentrating</li>
						<li>Change in weight/appetite </li>
						<li>Somatic symptoms</li></ul>
						
