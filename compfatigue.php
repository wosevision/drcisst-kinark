						<h4>Compassion Fatigue Trajectory</h4>
						<ul><li>The Zealot Phase</li>
						<li>The Irritability Phase</li>
						<li>The Withdrawal Phase</li>
						<li>The Zombie Phase</li>
						<li>Pathology vs. Renewal/Maturation</li></ul>

						<h4>Zealot Phase</h4>
						<ul><li>Committed, involved, available</li>
						<li>Solving problems/making a difference</li>
						<li>Willingly go the “extra mile”</li>
						<li>High enthusiasm</li>
						<li>Volunteers without being asked</li></ul>

						<h4>The Irritability Phase</h4>
						<ul><li>Begin to cut corners</li>
						<li>Begin to avoid clients/patients</li>
						<li>Begin to denigrate the people we serve</li>
						<li>Use of humour is inappropriate</li>
						<li>Oversights, mistakes and lapses of concentration</li>
						<li>Start distancing ourselves from friends and coworkers</li></ul>

						<h4>The Withdrawal Phase</h4>
						<ul><li>Enthusiasm turns sour</li>
						<li>Clients become irritants, instead of persons</li>
						<li>We make complaints about our work life and our personal life</li>
						<li>Tired all the time, don’t want to talk about what we do.</li>
						<li>We start to neglect our family, clients, coworkers and ourselves</li>
						<li>We try to avoid our pain and sadness</li></ul>

						<h4>The Zombie Phase</h4>
						<ul><li>Our hopelessness turns to rage</li>
						<li>Others appear incompetent or ignorant to us</li>
						<li>We develop a real distain for our clients</li>
						<li>We have...no patience...no sense of humor...no time for fun</li></ul>
						
