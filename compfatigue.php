						<h4>A Word about Sleep</h4>
						<ul><li>Sleep disruption is both:
						<ul><li>A key indicator of Compassion Fatigue, and</li>
						<li>One of the primary causes of Compassion Fatigue</li></ul></li>
						<li>Sleep Disruption is a key symptom of Compassion Fatigue
						<ul><li>When a care giver’s sleep pattern is disrupted for an extended period of time, this is a key warning sign that something is wrong. If normal sleep patterns aren’t reestablished, this combined with other symptoms may indicate that the caregiver is developing “Compassion Fatigue”.</li></ul></li>
						<li>DisruptionofNormalSleepPatternscanbea primary cause of Compassion Fatigue
						<ul><li>It is during normal “deep” sleep that much of the processing of the traumatic experiences occur. When sleep is disrupted (either shorten or disturbed), the traumatic experience can become lodged in the sympathetic nervous system. Over time an accumulation of these unprocessed traumatic experiences can lead to Compassion Fatigue.</li></ul>

