                    <h2>CRITICAL INCIDENT STRESS MANAGEMENT</h2>
                    <h4>“Helping The Helpers”</h4>
    
                    <blockquote></blockquote>
    
                    <h4>REMEMBER these things about a critical incident in your life...</h4>
                    <ul>
                        <li>You are having a normal reaction to an abnormal event.</li>
                        <li>You are not losing it, you are finding a sense of self and your sense of decency.</li>
                        <li>You are not the only one experiencing these reactions right now.</li>
                        <li>You are not less capable or less competent. You are not weak, a wimp or unstable.</li>
                        <li>You are NOT alone.</li>
                    </ul>
    
                    <blockquote></blockquote>
    
                    <h4>STRESS REACTION...</h4>
                    <p><b>ACUTE</b></p>
                    <p>Response to a specific incident Immediate reaction or within three days</p>
                    <p><b>DELAYED</b></p>
                    <p>Out of the blue</p>
                    <p>Seems unconnected to any incident</p>
                    <p><b>ACCUMULATED</b></p>
                    <p>Built up over a long period of time Gradual erosion of coping resources</p>
    
                    <h4>ALLEVIATING STRESS...</h4>
                    <p>Begins with owning your stress responses. Not... owning the event...</p>
                    <ul>
                        <li>Verbalize your frustrations. Talking about the obstacles that create frustration often leads to an increased ability to cope.
                        <li>Figure out why you are reacting, The better you understand yourself, the better you will adjust.
                        <li>Identify obstacles that block you.
                        <li>Plan strategies to go around obstacles.
                        <li>Be persistent to reach your goal, working smarter not harder. þBe prepared to accept some degree of failure.
                        <li>Search for benefits when stressors arise.
                        <li>Laughter is good medicine for the mind.
                        <li>Play.
                        <li>Ask for help from people you care about - you will return the gift. þLearn to compromise.
                        <li>Listen to others’ needs, but decide when and IF you can meet them.
                        <li>Give yourself credit for what you do well and acknowledge what you don’t do well.
                        <li>Accept responsibility for your actions, feeling and thoughts. þSet limits for your external and internal worlds.
                        <li>Accept that you are less than perfect.
                        <li>Love and be loved.
                    </ul>
    
                ￼    <h4>THINGS TO TRY...</h4>
                    <ul>
                        <li>Strenuous physical exercise, alternating with relaxation will alleviate some of the physical reaction.</li>
                        <li>You’re normal and having normal reactions - don’t label yourself crazy.</li>
                        <li>Talk to people - talk is the most healing medicine.</li>
                        <li>Be aware of numbing the pain with overuse of drugs or alcohol, you don’t need to complicate this with a substance abuse problem.</li>
                        <li>Reach out - people do care.</li>
                        <li>Maintain as normal a schedule as possible, structure your time, keep busy.</li>
                        <li>Spend time with others.</li>
                        <li>Help your co-workers as much as possible by sharing feelings and checking out how they are doing.</li>
                        <li>Give yourself permission to feel rotten and share your feelings with others.</li>
                        <li>Do things that feel good to you.</li>
                        <li>Realize those around you are under stress.</li>
                        <li>Don’t make any big life changes.</li>
                        <li>Make as many daily decisions as possible which will give you a feeling of control over your life, ie. if someone asks you what you want to eat - answer them even if you’re not sure.</li>
                        <li>Get plenty of rest.</li>
                        <li>Reoccurring thoughts, dreams or flashbacks are normal - don’t try to fight them - they will decrease over time and become less painful.</li>
                        <li>Eat well-balanced and regular meals (even if you don’t feel like it).</li>
                    </ul>
    
                    <h4>BENEFITS OF EXERCISE...</h4>
                    <p>Regular sessions of physical conditioning - aerobic exercises contribute to these benefits...</p>
                    <p><b>Increased</b> efficiency of delivery of oxygen to tissues</p>
                    <p><b>Lowered</b> heart rate and blood pressure</p>
                    <p><b>New growth</b> of capillaries to carry more blood to muscles Reduction of body fat</p>
                    <p><b>Increased</b> ability to deal with stress</p>
                    <p><b>Flexibility</b>, endurance and strength</p>
                    <p><b>Resistance</b> to stress, disease, illness</p>
                    <p><b>Better</b> posture, body shape, complexion</p>
                    <p><b>Better</b> tolerance of anxiety</p>
                    <p><b>Self-esteem</b> and feeling of fitness and health</p>
        
                    <p><b>EXERCISE DO’S...</b></p>
                    <p>Make exercise part of your life; set aside scheduled time for your programme.</p>
                    <p>Select activities you enjoy; have fun when exercising.</p>
                    <p>Maximize opportunities for activity; replace sedentary habits.</p>
                    <p>Change your eating habits; being overweight is also a serious health problem.</p>
                    <p>Warm up and cool down gradually; three to five minutes before and after vigorous exercise.</p>
                    <p>Be sensible about clothing and replacing fluids.</p>
                    <p>Keep up the good work; motivate yourself, exercise with a friend or a club, read up on your activity.</p>
                    <p><b>EXERCISE DON'T’S...</b></p>
                    <p>Don’t look at exercise as a “crash” program.</p>
                    <p>Don’t set unrealistic goals.</p>
                    <p>Don’t exercise for at least one to two hours after eating.</p>
                    <p>Don’t continue to exercise if you notice an injury or when you’re ill or suffering from an infection.</p>
                    <p>Don’t hurry or rush your programme; start gradually, build up your time.</p>
                    <p>Don’t forget to check with your doctor before beginning your programme, especially after age 30.</p>
    
                    <h4>SUGGESTED DIETARY CHANGES...</h4>
                    <ol>
                        <li>Eat less fat - less red meat, more poultry, fish; less butter and lard.</li>
                        <li>Reducesaturatedfats-thatarehighestintop-qualityred meats, organ meats, butter, egg yolks, cheeses, lard, hydrogenated fats; use vegetable oil or margarine.</li>
                        <li>Reduce protein intake to less than 300g per day - consider other protein sources; tofu, beans(legumes), soybeans, corn, barley, oatmeal, whole wheat. Eat the right type of carbohydrates: brown or wild rice, whole wheat or stone ground breads or pita breads and whole wheat pasta.</li>
                        <li>Eat more fibre - such as whole bran or whole grain. Increase your intake of fruits and vegetables, eaten raw if possible.</li>
                        <li>Reduce refined sugars, flours - sugar levels are high in soft drinks and desserts, also in many condiments, salad dressings, sauces, breads and crackers. Sugar is needed for energy but it is better to it get from complex carbohydrates found in bread and vegetables rather than from the simple sugars such as table sugar, honey and molasses, which raise blood/body fat and cholesterol levels.</li>
