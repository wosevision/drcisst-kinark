                    <h4>CRITICAL INCIDENT STRESS...</h4>
                    <p>is a normal reaction to sudden and unexpected events beyond that range of normal human experience. On average, 80% of individuals exposed to a critical incident will experience a reaction.</p>
                    <p>In its mildest form, this stress can disrupt an individual’s day-to-day functioning; at its most severe, this stress reaction may become chronic and have serious, long-term health and relationship consequences.</p>
                    <p>How an individual reacts varies from person to person and depends on several factors: the type of event, its intensity and duration, the suddenness with which it occurs, past experiences, coping strategies and social supports. These reactions can cause additional stress in the sufferer, their families and co-workers. If they are left un-managed...REACTIONS CAN BECOME SYMPTOMS. It is a wise and healthy choice to take care NOW. With understanding, support and care of yourself (and by people who care about you), stress reactions usually pass more quickly. Occasionally the traumatic event is so painful that professional assistance from a physician or therapist may be necessary, this does not mean that a person is weak or crazy - it simply indicates that the reaction to this particular event was powerful.</p>
    
